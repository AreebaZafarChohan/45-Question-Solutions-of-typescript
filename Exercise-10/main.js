// this programs add 2 numbers through condition
var num1 = 7;
var num2 = 3;
var operator = '+';
// appling condition
if (operator == "+") {
    console.log(num1 + num2);
}
else if (operator == "-") {
    console.log(num1 - num2);
}
else if (operator == "*") {
    console.log(num1 * num2);
}
else if (operator == "/") {
    console.log(num1 / num2);
}
else if (operator == "%") {
    console.log(num1 % num2);
}
else {
    console.log("Invalid Operation");
}
