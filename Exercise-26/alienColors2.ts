// Running the if block 
let alienColor : string = "red";

if (alienColor === "green") {
    console.log("Congratulations! You just earned 5 points.");
} else {
    console.log("Congratulations! You just earned 10 points.");
}

// Running the else block
 alienColor = "red";

 if (alienColor === "green") {
    console.log("Congratulations! You just earned 5 points.");
 } else {
    console.log("Congratulations! You just earned 10 points.");
}